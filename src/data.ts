// Dynamically import all fanart from src/assets/fanart
const fanartModules = import.meta.glob('@/assets/fanart/*.{webp,jpg,png,mp4,gif}', {
    eager: true,
    import: 'default'
});

export const FANART_ITEMS = Object.entries(fanartModules).map(([path, url], index) => {
    const fileName = path.split('/').pop() || "";
    const isVideo = fileName.toLowerCase().endsWith('.mp4');

    // Auto-generate consistent but varied spans for a 4-column dense grid
    let span = "col-span-1 row-span-1"; // Standard
    if (index % 11 === 0) span = "md:col-span-2 md:row-span-2"; // Rare Big square
    else if (index % 7 === 0) span = "md:col-span-2 md:row-span-1"; // Wide
    else if (index % 5 === 0) span = "md:col-span-1 md:row-span-2"; // Tall

    return {
        id: index + 1,
        type: isVideo ? "video" : "image",
        title: fileName.split('.')[0].replace(/[_-]/g, ' '),
        desc: "Royal Archive Collection",
        url: url as string,
        span: span
    };
}).sort(() => Math.random() - 0.5); // Shuffle the entire collection

// SQUARE_DATA for Home Hero - Take 16 random items from the fanart collection
export const SQUARE_DATA = [...FANART_ITEMS]
    .filter(item => item.type === 'image') // Ensure only images for the hero grid
    .slice(0, 16)
    .map((item, idx) => ({
        id: idx + 1,
        src: item.url
    }));
